<h2>Frequently Asked Questions:</h2>

<ul>
<li><b>How do I write a summary?</b> Find the paper using the search box (Click the box to add it from other databases if it does not already have a summary).
<li><b>I cannot find a paper</b> Search for papers using the full title, the DOI, or ar$\large\chi$iv id. 
The search can access all papers in the 
<a href="http://www.crossref.org/">Digital Object Identifier (DOI)</a>, 
<a href="http://arxiv.org">ar$\large\chi$iv</a>, 
<a href="http://bibsonomy.org">Bibsonomy</a>, and
<a href="http://dblp.uni-trier.de/">dblp</a> databases
<li><b>How can I display my name instead of my username?</b> In the settings for your user account you can set a Display Name which will show up along side your summaries.
<li><b>How do I write math?</b> Write latex the same way you do in a paper by placing it between <span>$</span>'s anywhere. You can use <span>$</span><span>$</span> to center the text.
<li><b>How do I set my profile icon?</b> Set up a <a href="https://gravatar.com">Gravitar</a> with the same email you used to sign up.
<li><b>Can I post anonymously?</b> You can choose to mark your summary anonymous and hide your name.
<li><b>Can I view the source of another summary?</b> Click on the <span class="glyphicon glyphicon-eye-open" aria-hidden="true"></span> icon next to a summary to show the source.
<li><b>How can I receive alerts when new summaries are posts?</b> Subscribe to our 
<a href="http://www.shortscience.org/rss.xml">RSS</a> feed or follow us on 
<a href="https://twitter.com/shortscienceorg">Twitter</a> and 
<a href="https://www.facebook.com/shortscienceorg/">Facebook</a> to stay up to date.
<li><b>How long should a summary be?</b> A summary should be as long as it needs to be.

</ul>

<h2 id="rules">What a summary should have:</h2>

<ul>
<li>A description of the paper as if you were explaining the concept to a colleague.
<li>Start off the summary with a high level description of the paper.
<li>Explain why the paper is significant.
<li>Use simple examples to break down the concept.
<li>Discuss how the paper fits into the larger field.
<li>Rewrite the formulations in the paper from a different perspective.

</ul>

<h2 id="rules2">What a summary should not have:</h2>

<ul>
<li>Do not post only a link or an image. There should be text discussing the paper as well.
<li>Links for more information or a source of the summary should appear at the end of the summary.
<li>Do not repeat the text of the abstract. 
</ul>


